#include "Node.hpp"

//-------------------------------------------------------------------------------------------------------------------------------------------------

Node::Node(int pLon, int pLat) : longitude(pLon),latitude(pLat){
    this->tags = new QHash<QString,QString>;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

int Node::getLon() const {
    return this->longitude;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

int Node::getLat() const {
    return this->latitude;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

void Node::addTag(QString pK, QString pV) {
    this->tags->insert(pK,pV);
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

QHash<QString, QString>* Node::getTags() {
    return(this->tags);
}

