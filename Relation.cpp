#include "Relation.hpp"

//-------------------------------------------------------------------------------------------------------------------------------------------------

Relation::Relation() {
    this->memberList = new QList<Member*>;
    this->tags = new QHash<QString,QString>;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

void Relation::addMember(QString pType, QString pRef, QString pRole) {
    this->memberList->append(new Member(pType,pRef,pRole));
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

void Relation::addTag(QString pK, QString pV) {
    this->tags->insert(pK,pV);
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

QList<Member*>* Relation::getMemberList() {
    return(this->memberList);
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

QHash<QString,QString>* Relation::getTags() {
    return(this->tags);
}

