#include "Way.hpp"

//-------------------------------------------------------------------------------------------------------------------------------------------------

Way::Way() {
    this->tags = new QHash<QString,QString>;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

void Way::setNodeList(QList<Node*> *pNodeList) {
    this->nodeList = pNodeList;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

QList<Node*>* Way::getNodeList() {
    return(this->nodeList);
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

void Way::addTag(QString pK, QString pV) {
    this->tags->insert(pK,pV);
}

//-------------------------------------------------------------------------------------------------------------------------------------------------

QHash<QString,QString>* Way::getTags() {
    return(this->tags);
}

